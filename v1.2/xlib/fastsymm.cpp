void TFastSymmLib::processSymm(const TIntList& mind, int count, FastSymm& fs)  {
  for(int i=0; i < count; i++ )  {
    fs.DVs[i]  = MultDV[mind[i]];
    fs.FVs[i]  = MultFV[mind[i]];
    fs.IVs[i]  = MultIV[mind[i]];
    fs.DVTs[i] = MultDVT[mind[i]];
    fs.FVTs[i] = MultFVT[mind[i]];
    fs.IVTs[i] = MultIVT[mind[i]];
    fs.DAs[i]  = MultDA[mind[i]];
    fs.FAs[i]  = MultFA[mind[i]];
    fs.IAs[i]  = MultIA[mind[i]];
    fs.DATs[i] = MultDAT[mind[i]];
    fs.FATs[i] = MultFAT[mind[i]];
    fs.IATs[i] = MultIAT[mind[i]];
  }
}
